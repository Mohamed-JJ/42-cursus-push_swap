#include "push_swap.h"

int	check_sign(char **a)
{
	int	i;
	int	j;
	int	c;
	int	c1;

	i = 0;
	while (a[i])
	{
		c = 0;
		c1 = 0;
		j = 0;
		while (a[i][j])
		{
			if (a[i][j] == '+' || a[i][j] == '-')
				c++;
			if (a[i][j] >= '0' && a[i][j] <= '9')
				c1++;
			j++;
		}
		if (c > 1 || (c >= 1 && c1 == 0))
			return (1);
		i++;
	}
	return (0);
}
